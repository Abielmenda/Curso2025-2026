"""**TASK 7.1a: For all classes, list each classURI. If the class belogs to another class, then list its superclass.**
**Do the exercise in RDFLib returning a list of Tuples: (class, superclass) called "result". If a class does not have a super class, then return None as the superclass**
"""
# Visualize the results
result = [] #list of tuples

for allClasses in g.subjects(RDF.type, RDFS.Class): # Recorro todas las clases
    superclass = None # Var aux por si tiene superclase
    for super in g.objects(allClasses, RDFS.subClassOf): # Si tiene atributo subClassOf, entonces tiene superclase
        superclass = super
        result.append((allClasses, superclass))
    if superclass is None:
        result.append((allClasses, None))

for r in result:
  print(r)

## Validation: Do not remove
report.validate_07_1a(result)

"""**TASK 7.1b: Repeat the same exercise in SPARQL, returning the variables ?c (class) and ?sc (superclass)**"""

query = """
PREFIX rdfs: <http://www.w3.org/2000/01/rdf-schema#>
SELECT ?c ?sc
WHERE {
  ?c a rdfs:Class .
  OPTIONAL { ?c rdfs:subClassOf ?sc . }
}
"""

for r in g.query(query):
  print(r.c, r.sc)

## Validation: Do not remove
report.validate_07_1b(query,g)

"""**TASK 7.2a: List all individuals of "Person" with RDFLib (remember the subClasses). Return the individual URIs in a list called "individuals"**

"""

ns = Namespace("http://oeg.fi.upm.es/def/people#")

# variable to return
individuals = []
classes = {ns.Person} # variable para guardar las clases, empezando por person
for subPerson in g.subjects(RDFS.subClassOf, ns.Person):
    classes.add(subPerson) # se agregan subclases de person
    for sub2Person in g.subjects(RDFS.subClassOf, subPerson):
        classes.add(sub2Person) # se agregan subclases de las subclases de person

for cla in classes: # Ya con todas las clases se muestran los individuos de cada una
    for indiv in g.subjects(RDF.type, cla):
        individuals.append(indiv)
# visualize results
for i in individuals:
  print(i)

# validation. Do not remove
report.validate_07_02a(individuals)

